    public static String lerarq(String caminho) throws IOException {
        String texto = "";
        String src;
        File arquivo = new File(caminho);
        BufferedReader in = new BufferedReader(new FileReader(arquivo));
        while ((src = in.readLine()) != null) {
            texto += src;
        }
        in.close();
        return texto;
    }

    public static void escreverarq(String caminho, String texto) throws IOException {

        File arquivo = new File(caminho);
        try (PrintWriter pwriter = new PrintWriter(arquivo)) {
            pwriter.write(texto + "\n");
            pwriter.close();
        }
    }
    public static String esconder(String mensagem){

        String resultado = "";
        for (int i = 0; i < mensagem.length(); i++) {
            int temp = mensagem.charAt(i);

            resultado += (char) (temp+13);
        }
        return resultado;
    }
    public static String mostrar(String mensagem){

        String resultado = "";
        for (int i = 0; i < mensagem.length(); i++) {
            int temp = mensagem.charAt(i);
            resultado += (char) (temp-13);
        }
        return resultado;

    }
