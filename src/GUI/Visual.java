    private void btnatualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnatualizarActionPerformed
        if (tfmatricula.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Digite a matricula do aluno!!!", "___Pesquisa___", WIDTH);
        }
        if (alunos.containsKey(tfmatricula.getText())) {
            alunos.remove(tfmatricula.getText());
            Aluno aluno = new Aluno(tfmatricula.getText(), tfnome.getText(), tfturma.getText(),Integer.parseInt(jlstrike.getText()));
            alunos.put(tfmatricula.getText(), aluno);
            try {
                    String novamensagem = "";
                    String abc = Visual.lerarq("alunos2.txt");
                    String[] lista = abc.split("#");
                    for (String lista1 : lista) {
                        String[] lista2 = lista1.split(",");
                        if (mostrar(lista2[0]).equals(tfmatricula.getText())) {
                            String dados = esconder(tfmatricula.getText()) + "," + esconder(tfnome.getText()) + "," + esconder(tfturma.getText()) + "," + jlstrike.getText() + "#";
                            novamensagem += dados;
                        }else{
                            novamensagem += lista1+"#";
                        }
                    }
                    escreverarq("alunos2.txt", novamensagem);
                } catch (IOException io) {
                    JOptionPane.showMessageDialog(this,"Exceção IO \n"+io.getMessage(), "___Exceção___", WIDTH);
                }
            JOptionPane.showMessageDialog(this, "Aluno atualizado com sucesso!!!", "___Atualizado___", WIDTH);
        }
    }//GEN-LAST:event_btnatualizarActionPerformed

    private void btncadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastrarActionPerformed
        if (tfmatricula.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Digite a matricula do aluno!!!", "___Pesquisa___", WIDTH);
        }
        if (!alunos.containsKey(tfmatricula.getText())) {
        try {
            
            if (!tfmatricula.getText().equals("")) {
                Aluno aluno = new Aluno(Integer.parseInt(tfmatricula.getText())+"", tfnome.getText(), tfturma.getText(),Integer.parseInt(jlstrike.getText()));
                alunos.put(tfmatricula.getText(), aluno);
                String dadoaluno = esconder(tfmatricula.getText()) + "," + esconder(tfnome.getText()) + "," + esconder(tfturma.getText())  + ","+ jlstrike.getText() +"#\n";

                String textoantigo = lerarq("alunos2.txt");
                escreverarq("alunos2.txt", textoantigo + dadoaluno);

                JOptionPane.showMessageDialog(this, "Aluno salvo com sucesso!!!", "___Cadastro___", WIDTH);
            }
            

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Exceção " + e.getMessage() + "\n"
                    + "digte uma matricula válida", "___Exceção___", WIDTH);
        } catch (IOException ex) {
            Logger.getLogger(Visual.class.getName()).log(Level.SEVERE, null, ex);
        }
        } else {
            JOptionPane.showMessageDialog(this, "Matricula ja está cadastrada no sistema!!!", "___Cadastro___", WIDTH);
        }
    }//GEN-LAST:event_btncadastrarActionPerformed

